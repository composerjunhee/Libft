#include "libft.h"

int ft_memcmp(const void *s1, const void *s2, size_t num)
/* s1 is dst, s2 is src*/
{
	size_t	i;
	unsigned char	*tmp1;
	unsigned char	*tmp2;

	i = 0;
	tmp1 = (unsigned char *)s1;
	tmp2 = (unsigned char *)s2;
	while (i < num)
	{
		if (tmp1[i] != tmp2[i])
			return (tmp1[i] - tmp2[i]);
		i++;
	}
	return (0);
}
